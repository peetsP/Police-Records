    public void Load_Room()
    {
        int c;
        try {
            pst = con.prepareStatement("select * from occurencebook");
            ResultSet rs = pst.executeQuery();
            
            ResultSetMetaData rsd = rs.getMetaData();
            c = rsd.getColumnCount();
            
            d = (DefaultTableModel) jTable1.getModel();
            
            d.setRowCount(0);
            
            while(rs.next())
            {
                Vector v2 = new Vector();
                for(int i = 1; i <= c; i++)
                {
                    v2.add(rs.getString("serialno"));
                    v2.add(rs.getString("refno"));
                    v2.add(rs.getString("casefno"));
                    v2.add(rs.getString("time"));
                    v2.add(rs.getString("natureofo"));
                }
                d.addRow(v2);
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(OBJTable.class.getName()).log(Level.SEVERE, null, ex);
        }   
    }
    

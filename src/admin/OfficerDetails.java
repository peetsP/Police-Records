    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        // SAVE BUTTON
        boolean proceed = false;

        String name = oname.getText();
        String rank = orank.getText();
        String servicenumber = oserviceno.getText();
        String password = opassword.getText();
        

        if(name.equals("")){
            
            proceed = false;
            lblerror.setText("Insert Name!");
        }
        else if(rank.equals("")){
            proceed = false;
            lblerror.setText("Insert Rank");
        }
        else if(servicenumber.equals("")){
            proceed = false;
            lblerror.setText("Insert Service Number");
        }
        else if(password.equals("")){
            proceed = false;
            lblerror.setText("Insert Password");
        }
        
        else
        proceed  = true;
        if(proceed){
            try {
                pst = con.prepareStatement("insert into users(name, rank, serviceno, password)values(?,?,?,?)");
                pst.setString(1, name);
                pst.setString(2, rank);
                pst.setString(3, servicenumber);
                pst.setString(4, password);

                pst.executeUpdate();
                JOptionPane.showMessageDialog(this, "Officer Information Added!");

                oname.setText("");
                orank.setText("");
                oserviceno.setText("");
                opassword.setText("");

            } catch (SQLException ex) {
                Logger.getLogger(OfficerDetails.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
    }//GEN-LAST:event_jButton1ActionPerformed


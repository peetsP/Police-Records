    private void btnaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaddActionPerformed

        // SAVE BUTTON
        boolean proceed = false;
        String serialno = ambsn.getText();
        SimpleDateFormat date1 = new SimpleDateFormat("yyyy-MM-dd");
        String Dategiven = date1.format(ambdate.getDate());
        String serviceno = ambserviceno.getText();
        String officerrank = ambrank.getText();
        String oname = ambname.getText();
        String wtype = ambtype.getText();
        String bodynumber = ambbtype.getText();
        String quantityIssued = ambquantity.getText();
        String desc = ambdesc.getText();
        String quantreturned = ambreturnqty.getText();
        SimpleDateFormat date2 = new SimpleDateFormat("yyyy-MM-dd");
        String returnDate = date2.format(ambreturndate.getDate());
        String finalremarks = ambremarks.getText();

        if(serialno.equals("")){
            proceed = false;
            lblmessage.setText("Serial no is empty!");
        }
        else if(Dategiven.equals("")){
            proceed = false;
            lblmessage.setText("Date of Issue is empty!");
        }

        else if(serviceno.equals("")){
            proceed = false;
            lblmessage.setText("Service Number is Empty!");
        }
        else if(officerrank.equals("")){
            proceed = false;
            lblmessage.setText("Officer Rank is not Filled!");
        }
        else if(oname.equals("")){
            proceed = false;
            lblmessage.setText("Officer Name is Empty!");
        }
        else if(wtype.equals("")){
            proceed = false;
            lblmessage.setText("Weapon type is Empty!");
        }
        else if(bodynumber.equals("")){
            proceed = false;
            lblmessage.setText("Body Number is Empty!");
        }
        else if(quantityIssued.equals("")){
            proceed = false;
            lblmessage.setText("Quantity Issued is Empty!");
        }
        else if(desc.equals("")){
            proceed = false;
            lblmessage.setText("Description is Empty!");
        }
        else if(quantreturned.equals("")){
            proceed = false;
            lblmessage.setText("Quantity Returned is Empty!");
        }
        else if(returnDate.equals("")){
            proceed = false;
            lblmessage.setText("Return Date is Empty!");
        }
        else if(finalremarks.equals("")){
            proceed = false;
            lblmessage.setText("Final Remarks   is Empty!");
        }
        else
        proceed  = true;
        if(proceed){
            try {
                pst = con.prepareStatement("insert into ArmsMovementBook(serialno, dateIssued, serviceno, rank, name, type, bodyno, quantityissued, description, quantityreturned, datereturned, remarks) values (?,?,?,?,?,?,?,?,?,?,?,?)");

                pst.setString(1, serialno);
                pst.setString(2, Dategiven);
                pst.setString(3, serviceno);
                pst.setString(4, officerrank);
                pst.setString(5, oname);
                pst.setString(6, wtype);
                pst.setString(7, bodynumber);
                pst.setString(8, quantityIssued);
                pst.setString(9, desc);
                pst.setString(10, quantreturned);
                pst.setString(11, returnDate);
                pst.setString(12, finalremarks);

                pst.executeUpdate();
                JOptionPane.showMessageDialog(this,"Arms Record Updated!");
                
                
               // ambsn.setText("");
                ambdate.setDateFormatString("");
                ambserviceno.setText("");
                ambrank.setText("");
                ambname.setText("");
                ambtype.setText("");
                ambbtype.setText("");
                ambquantity.setText("");
                ambdesc.setText("");
                ambreturnqty.setText("");
                ambreturndate.setDateFormatString("");
                ambremarks.setText("");

            } catch (SQLException ex) {
                Logger.getLogger(OccurenceBook.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnaddActionPerformed

  

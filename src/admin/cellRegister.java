    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed

        // Save Button for CR
        
        //Setting boolean
        
        boolean empty = false;
        
        String serialnumbcr = txtsncr.getText();
        String prisoner = txtnameop.getText();
        String crnumb = txtcrnocr.getText();
        String offense = txtoffense.getText();

        String obnoonadm = txtobnoadm.getText();
        SimpleDateFormat doadm = new SimpleDateFormat("yyyy-MM-dd");
        String StartDate = doadm.format(dateadm.getDate());
        String timeadm = txttimeadm.getText();
        String condition = txtconditionadm.getText();

        String obnorem1 = txtobnorema.getText();
        String obnorem2 = txtobnoremb.getText();
        String obnorem3 = txtobnoremc.getText();

        String obondisp = txtobnodisp.getText();
        SimpleDateFormat dispDate = new SimpleDateFormat("yyyy-MM-dd");
        String EndDate = dispDate.format(dateadm.getDate());

        String disposedT = txttimedisp.getText();
        String disposal = txtdisposal.getText();
        String conditiondisp = txtconditiondisp.getText();

        if(serialnumbcr.equals("")){
            empty = false;
            lblmessage2.setText("Serial No is Empty!");
        }
        
        else if(prisoner.equals("")){
            empty = false;
            lblmessage2.setText("Name of Prisoner is Empty!");
        }
        
        else if(crnumb.equals("")){
            empty = false;
            lblmessage2.setText("Crime Number is Empty!"); 
        }
        
        else if(offense.equals("")){
            empty = false;
            lblmessage2.setText("Offense details is not Filled!");
        }
        
        else if(obnoonadm.equals("")){
            empty = false;
            lblmessage2.setText("OB No is not inserted!");
        }
        
        else if(StartDate.equals("")){
            empty = false;
            lblmessage2.setText("Admissin Date is not inserted!");
        }
        
        else if(timeadm.equals("")){
            empty = false;
            lblmessage2.setText("Admission time is not inserted!");
        }
        
        else if(condition.equals("")){
            empty = false;
            lblmessage2.setText("Condition on Admission is not inserted!");
        }
        
        else if(obondisp.equals("")){
            empty = false;
            lblmessage2.setText("Disposal OB no is not inserted!");
        }
        
        else if(EndDate.equals("")){
            empty = false;
            lblmessage2.setText("Removal Date is not inserted!");
        }
        
        else if(disposedT.equals("")){
            empty = false;
            lblmessage2.setText("Disposal Time is not inserted!");
        }
        
        else if(disposal.equals("")){
            empty = false;
            lblmessage2.setText("Disposed method is not inserted!");
        }
        
        else if(conditiondisp.equals("")){
            empty = false;
            lblmessage2.setText("Condition is not filled!");
        }
        
        else
            empty = true;
        
        if(empty) {
        
        try {
            pst = con.prepareStatement("insert into cellregister (serialnocr, nameofp, crno, offense, obnoadm, date, time, conditionadm, obnorea, obnoreb, obnorec, obnodisp, datedisp, timedisp, disposal, conditiondisp) values(?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?)");
            
            pst.setString(1, serialnumbcr);
            pst.setString(2, prisoner);
            pst.setString(3, crnumb);
            pst.setString(4, offense);
            pst.setString(5, obnoonadm);
            pst.setString(6, StartDate);
            pst.setString(7, timeadm);
            pst.setString(8, condition);
            pst.setString(9, obnorem1);
            pst.setString(10, obnorem2);
            pst.setString(11, obnorem3);
            pst.setString(12, obondisp);
            pst.setString(13, EndDate);
            pst.setString(14, disposedT);
            pst.setString(15, disposal);
            pst.setString(16, conditiondisp);
            
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "prisoner Record Added");
            
            txtnameop.setText("");
            txtcrnocr.setText("");
            txtoffense.setText("");
            txtobnoadm.setText("");
            txttimeadm.setText("");
            txtconditionadm.setText("");
            txtobnorema.setText("");
            txtobnoremb.setText("");
            txtobnoremc.setText("");
            txtobnodisp.setText("");
            txttimedisp.setText("");
            txtdisposal.setText("");
            txtconditiondisp.setText("");
         
        } catch (SQLException ex) {
            Logger.getLogger(cellRegister.class.getName()).log(Level.SEVERE, null, ex);
        }
        }
    }//GEN-LAST:event_jButton4ActionPerformed


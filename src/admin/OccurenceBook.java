    private void btnaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaddActionPerformed
        
        // SAVE BUTTON
        boolean proceed = false;
        String serialno = txtsn.getText();
        String referenceno = txtrefno.getText();
        String casefileno = txtcasefileno.getText();
        String natureofoccurence = txtnature.getText();
        String timeob = txtobtime.getText();
        
        if(serialno.equals("")){
            proceed = false;
            lblmessage.setText("Serial no is empty!");
        }
        else if(referenceno.equals("")){
            proceed = false;
            lblmessage.setText("Ref no is empty!");
        }
        
        else if(casefileno.equals("")){
            proceed = false;
            lblmessage.setText("Case File Number is Empty!");
        }
        else if(timeob.equals("")){
            proceed = false;
            lblmessage.setText("Time is not Filled!");
        }
        else if(natureofoccurence.equals("")){
            proceed = false;
            lblmessage.setText("Nature of Occurence is Empty!");
        }
        else
            proceed  = true;
        if(proceed){
         try {
        pst = con.prepareStatement("insert into occurencebook(serialno, refno, casefno,natureofo, time) values (?,?,?,?,?)");

        pst.setString(1, serialno);
        pst.setString(2, referenceno);
        pst.setString(3, casefileno);
        pst.setString(4, natureofoccurence);
        pst.setString(5, timeob);

        pst.executeUpdate();
        JOptionPane.showMessageDialog(this,"OB Updated");

        txtrefno.setText("");
        txtcasefileno.setText("");
        txtnature.setText("Type Nature Of Occurence Here....");
        txtobtime.setText("");
        Load_JT();
           
            
        } catch (SQLException ex) {
            Logger.getLogger(OccurenceBook.class.getName()).log(Level.SEVERE, null, ex);
        }   
        }  
    }//GEN-LAST:event_btnaddActionPerformed
    
    
    
